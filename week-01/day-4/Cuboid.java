public class Cuboid {
  public static void main(String[] args) {
    double a = 10;
    double surfaceArea = 6 * (a * a);
    double volume = a * a * a;

    System.out.println("Surface Area: " + surfaceArea);
    System.out.println("Volume: " + volume);
  }
}
